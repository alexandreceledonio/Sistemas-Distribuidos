    private void button4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        String codigo = "";
        int a = jTable2.getSelectedRow();

        if (a < 0) {
            JOptionPane.showMessageDialog(null, "Selecionar Linha da Tabela");

        } else {
            int confirmar = JOptionPane.showConfirmDialog(null, "Deseja Excluir?");
            if (JOptionPane.OK_OPTION == confirmar) {
                try {
                    Registry registro = LocateRegistry.getRegistry("127.0.0.1", 1099);
                    RMIBD interfac = (RMIBD) registro.lookup("rmi://localhost:1099/RMIBD");
                    codigo = (String) modelo.getValueAt(a,0);
                    interfac.excluir(codigo);
                    modelo.removeRow(a);
                    JOptionPane.showMessageDialog(null, "Registro Excluido!");
                 }catch(RemoteException | NotBoundException e){
                     JOptionPane.showMessageDialog(null, e);
                 }
            }
        }
    }//GEN-LAST:event_button4ActionPerformed


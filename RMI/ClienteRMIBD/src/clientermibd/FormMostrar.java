    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        try {
            Registry registro = LocateRegistry.getRegistry("127.0.0.1", 1099);
            RMIBD interfac = (RMIBD) registro.lookup("rmi://localhost:1099/RMIBD");
            String cod;
            cod = textField1.getText();

            if (interfac.buscar_produto(cod).isEmpty()) {

                JOptionPane.showMessageDialog(null, "Não Encontrado");
            } else {
                textField2.setText(interfac.buscar_produto(cod).get(0).getNome());
                textField3.setText(interfac.buscar_produto(cod).get(0).getQuantidade());
            }

        } catch (RemoteException | NotBoundException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_button1ActionPerformed

    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        String cod, nom, quant;
        cod = textField1.getText();
        nom = textField2.getText();
        quant = textField3.getText();

        try {

            Registry registro = LocateRegistry.getRegistry("127.0.0.1", 1099);
            RMIBD interfac = (RMIBD) registro.lookup("rmi://localhost:1099/RMIBD");
            interfac.adicionar(cod, nom, quant);
            JOptionPane.showMessageDialog(null, "Registro Adicionado");
            while (modelo.getRowCount() > 0) {
                modelo.removeRow(0);
            }
            textField1.setText("");
            textField2.setText("");
            textField3.setText("");
            Object[] fila = new Object[3];
            for (int i = 0; i < interfac.mostrar().size(); i++) {
                fila[0] = interfac.mostrar().get(i).getCodigo();
                fila[1] = interfac.mostrar().get(i).getNome();
                fila[2] = interfac.mostrar().get(i).getQuantidade();
                modelo.addRow(fila);
                jTable2.setModel(modelo);
            }
        } catch (RemoteException | NotBoundException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_button2ActionPerformed

